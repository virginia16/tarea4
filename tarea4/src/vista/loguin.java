    private void btningresarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btningresarActionPerformed
         sqlusuario modSql = new sqlusuario();
         usuario mod = new usuario();
        
        String pass = new String(txtpassword.getPassword());

        if (!txtusuario.getText().equals("") && !pass.equals("")) {

             try {
                 String nuevoPass = hash.sha1(pass);
                 
                 mod.setUsuario(txtusuario.getText());
                 mod.setPassword(nuevoPass);
                 
                 if (modSql.loguin(mod)) {
                     
                     frmlog = null;
                     this.dispose();
                     
                     programa frmprogram = new  programa();
                     frmprogram.setVisible(true);
                     
                 } else {
                     JOptionPane.showMessageDialog(null, "Datos incorrectos");
                 }} catch (SQLException ex) {
                 Logger.getLogger(loguin.class.getName()).log(Level.SEVERE, null, ex);
             }
        
        } else {
         JOptionPane.showMessageDialog(null, "Debe ingresar su usuario y contrase√±a, si no esta registrado debe registrarse");
        }

    }//GEN-LAST:event_btningresarActionPerformed

